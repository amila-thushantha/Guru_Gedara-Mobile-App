package com.example.elementaryapp2.classes;

public class ShapeBox {
    public int shape;
    public int shapeColor;
    public int count;
    public boolean isCorrect;

    public ShapeBox(int shape, int shapeColor, int count, boolean isCorrect) {
        this.shape = shape;
        this.shapeColor = shapeColor;
        this.count = count;
        this.isCorrect = isCorrect;
    }
}
